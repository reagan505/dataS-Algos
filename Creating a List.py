# Creating a list
my_list = [1, 2, 3, "Hello", 4.5]

# Creating an empty list
empty_list = []

# Printing the lists
print(my_list)
print(empty_list)


# Accessing elements
print(my_list[0])  # Output: 1
print(my_list[3])  # Output: Hello



# Slicing the list
print(my_list[1:4])  # Output: [2, 3, 'Hello']


# Iterating through the list
for item in my_list:
    print("Item:", item)

