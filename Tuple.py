# Creating a tuple
person = ("Alice", 30, "Engineer")

# Accessing elements
name = person[0]
age = person[1]
occupation = person[2]

print(f"Name: {name}, Age: {age}, Occupation: {occupation}")

# Slicing the tuple
print(person[1:])  # Output: (30, 'Engineer')

