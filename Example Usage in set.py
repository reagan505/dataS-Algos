# Creating a set of fruits
fruits = {"apple", "banana", "orange"}

# Adding a fruit
fruits.add("grape")
print("After adding grape:", fruits)

# Removing a fruit
fruits.remove("banana")
print("After removing banana:", fruits)

# Checking membership
if "apple" in fruits:
    print("Apple is in the set!")

# Displaying the final set
print("Final set of fruits:", fruits)
